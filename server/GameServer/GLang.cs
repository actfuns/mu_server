		public static bool LoadLangDict()
		{
			XElement LangXML = null;
			try
			{
				if (!File.Exists("Language.xml"))
				{
					return true;
				}
				LangXML = XElement.Load("Language.xml");
			}
			catch (Exception ex)
			{
				DataHelper.WriteFormatExceptionLog(ex, "LoadLangDict", true, false);
				return false;
			}
			bool result;
			try
			{
				if (null == LangXML)
				{
					result = false;
				}
				else
				{
					string LangType = Global.GetDefAttributeStr(LangXML, "LangType", "OtherLangText");
					Dictionary<int, string> langDict = new Dictionary<int, string>();
					Dictionary<string, string> langTextDict = new Dictionary<string, string>();
					IEnumerable<XElement> langItems = Global.GetSafeXElement(LangXML, "Languages").Elements();
					foreach (XElement langItem in langItems)
					{
						string otherText = Global.GetSafeAttributeStr(langItem, LangType);
						if (!string.IsNullOrEmpty(otherText))
						{
							langTextDict[Global.GetSafeAttributeStr(langItem, "ChineseText")] = otherText;
							int id = Convert.ToInt32(Global.GetDefAttributeStr(langItem, "ID", "0"));
							if (id != 0)
							{
								langDict[id] = otherText;
							}
						}
					}
					GLang.LangDict = langDict;
					GLang.LangTextDict = langTextDict;
					result = true;
				}
			}
			catch (Exception ex)
			{
				DataHelper.WriteFormatExceptionLog(ex, "LoadLangDict", true, false);
				result = false;
			}
			return result;
		}

		
		public static void OutputToFile()
		{
			IOrderedEnumerable<KeyValuePair<int, string>> list = from x in GLang.LanguageDict
			orderby x.Key
			select x;
			List<string> outList = new List<string>();
			foreach (KeyValuePair<int, string> item in list)
			{
				outList.Add(string.Format("  <Lang ID=\"{0}\" ChineseText=\"{1}\" OtherLangText=\"\" />", item.Key, item.Value));
			}
			File.WriteAllLines("language.txt", outList, Encoding.UTF8);
		}

		
		public static string GetLang(int id, params object[] args)
		{
			string text;
			string result;
			if (GLang.LangDict.TryGetValue(id, out text))
			{
				if (args.Length > 0)
				{
					result = string.Format(text, args);
				}
				else
				{
					result = text;
				}
			}
			else if (GLang.LanguageDict.TryGetValue(id, out text))
			{
				if (args.Length > 0)
				{
					result = string.Format(text, args);
				}
				else
				{
					result = text;
				}
			}
			else
			{
				result = string.Format("[未定义文本:{0}]", id);
			}
			return result;
		}

		
		public static string GetLang(string chineseText)
		{
			string otherLangText;
			string result;
			if (GLang.LangTextDict.TryGetValue(chineseText, out otherLangText))
			{
				result = otherLangText;
			}
			else
			{
				result = chineseText;
			}
			return result;
		}

		
		public static string UseGiftCodeMsg(int retCode)
		{
			if (retCode < 0)
			{
				if (retCode == -11003)
				{
					return GLang.GetLang("Server Error");
				}
				if (retCode == -11000)
				{
					return GLang.GetLang("服务器忙,请稍候再试");
				}
				switch (retCode)
				{
				case -22:
					return GLang.GetLang("参数错误");
				case -21:
					return GLang.GetLang("礼包码请求超时");
				case -20:
					return GLang.GetLang("验证次数过多，禁止验证24小时");
				case -18:
					return GLang.GetLang("当前区服不可使用该激活码");
				case -17:
					return GLang.GetLang("激活码已过期或不可使用");
				case -16:
					return GLang.GetLang("激活码使用失败，请确认使用渠道");
				case -15:
					return GLang.GetLang("激活码使用失败，请确认使用平台");
				case -14:
					return GLang.GetLang("该礼包码已经被使用");
				case -12:
					return GLang.GetLang("礼品码已经超过了最大使用次数限制，请输入其他的礼品码!");
				case -11:
					return GLang.GetLang("礼品码不存在，请输入有效的礼品码!");
				case -10:
					return GLang.GetLang("礼品码错误，请输入当前区的礼品码!");
				case -5:
					return GLang.GetLang("礼包码不可用");
				case -4:
					return GLang.GetLang("其他人正在校验");
				case -3:
					return GLang.GetLang("服务器错误");
				case -2:
					return GLang.GetLang("功能未开启");
				case -1:
					return GLang.GetLang("领取活动礼物时出错");
				}
			}
			else
			{
				if (retCode == 0)
				{
					return GLang.GetLang("激活成功，奖励已通过邮件发放");
				}
				if (retCode == 2)
				{
					return GLang.GetLang("背包空己满，奖励已通过邮件发放");
				}
			}
			return GLang.GetLang("领取活动礼物时出错");
		}

		
